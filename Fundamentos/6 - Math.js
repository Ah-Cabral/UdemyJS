//Aula breve sobre a classe Math

//Declarando constante de um raio de circunferência
const raio = 5.6; 

//Declarando constante referente à area de uma circunferência
const area = Math.PI * Math.pow(raio, 2); //PI é uma constante, já Math.pow é uma função que calcula a potência

//Dissecando a expressão:
const pi = Math.PI;

const pot = Math.pow(raio, 2)

//Printando a constante pi
console.log(pi);

//Printando a potência
console.log(pot)

//Por fim, printando a área
console.log(area);

//Math é um objeto
console.log(typeof Math);